package n

import "fmt"

/*============================================================================*
 * {{{ Public api 
 *============================================================================*/

func Log(message string, a ...interface{}) {
  fmt.Printf(message, a...)
  fmt.Printf("\n")
}

func Panic(message string, a ...interface{}) {
  var msg = fmt.Sprintf(message, a...)
  panic(msg)
}

