package urls 

import "fmt"

/*============================================================================*
 * {{{ Public api 
 *============================================================================*/
 
type UrlHelper struct {
}

/*============================================================================*
 * }}}
 *============================================================================*/

/*============================================================================*
 * {{{ Implementation
 *============================================================================*/
 
func newUrlHelper() *UrlHelper {
 	return &UrlHelper{}
}

func (*UrlHelper) url(msg string, args ...interface{}) string {
	var rtn = fmt.Sprintf(msg, args...)
	return rtn
}

